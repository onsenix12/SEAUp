"use client";

import { useState, useEffect, useRef } from "react";
import { useLanguage } from "@/contexts/LanguageContext";
import { useMusicFlow } from "@/contexts/MusicFlowContext";
import { useFacilitator } from "@/contexts/FacilitatorContext";
import { useRouter } from "next/navigation";
import Image from "next/image";
import { deriveMusicSkills } from "@/lib/learning/skills";
import SkillsCard from "@/components/learning/SkillsCard";
import FacilitatorPromptCard from '@/components/facilitator/FacilitatorPromptCard';
import { useFacilitatorPrompt } from '@/hooks/useFacilitatorPrompt';

export default function MusicResult() {
    const { language } = useLanguage();
    const { state, resetState, musicResult } = useMusicFlow();
    const { sessionData } = useFacilitator();
    const skills = deriveMusicSkills(state.soundEffects ?? [], state.hasRecordedAudio ?? false);
    const router = useRouter();
    const audioRef = useRef<HTMLAudioElement>(null);
    const [isPlaying, setIsPlaying] = useState(false);

    const { shouldShow, prompt: facilitatorPrompt, language: promptLanguage, dismiss } = useFacilitatorPrompt('result');
    const showCard = shouldShow;
    const [isSaving, setIsSaving] = useState(false);
    const [saved, setSaved] = useState(false);
    const [error, setError] = useState<string | null>(null);
    const [lyriaAvailable, setLyriaAvailable] = useState(true);
    const [audioSrc, setAudioSrc] = useState<string | null>(null);
    const [coverSrc, setCoverSrc] = useState<string | null>(null);
    const [story, setStory] = useState<string>('');
    const [musicPromptText, setMusicPromptText] = useState<string>('');

    const t = {
        title: language === 'en' ? 'Your Music' : 'Musikmu',
        subtitle: language === 'en' ? 'Generated by Lyria · Composed by You' : 'Dihasilkan oleh Lyria · Dikomposisi olehmu',
        story: language === 'en' ? 'Creation Story' : 'Cerita Penciptaan',
        save: language === 'en' ? 'Save to Gallery' : 'Simpan ke Galeri',
        saving: language === 'en' ? 'Saving...' : 'Menyimpan...',
        saved: language === 'en' ? 'Saved! ✓' : 'Tersimpan! ✓',
        tryAgain: language === 'en' ? 'Try Again' : 'Coba Lagi',
        lyriaUnavailable: language === 'en'
            ? 'Lyria is not yet enabled on this API key. Your cover artwork was generated — music generation will be available soon!'
            : 'Lyria belum diaktifkan pada kunci API ini. Karya seni sampulmu telah dihasilkan — pembuatan musik akan segera tersedia!',
        playPause: isPlaying
            ? (language === 'en' ? 'Pause' : 'Jeda')
            : (language === 'en' ? 'Play' : 'Putar'),
        sourceArtwork: language === 'en' ? 'Inspired by this artwork' : 'Terinspirasi dari karya ini',
        musicPrompt: language === 'en' ? 'Music Direction' : 'Arahan Musik',
        soundPalette: language === 'en' ? 'Your Sound Palette' : 'Palet Suaramu',
    };

    useEffect(() => {
        if (!musicResult) return;

        const { audioBase64, coverBase64, creationStory, musicPrompt: mp, lyriaAvailable: lyriaOk, lyriaError } = musicResult;

        // Show error if both audio failed and it's not just a Lyria unavailability
        if (!lyriaOk && lyriaError && !audioBase64) {
            setError(lyriaError);
        }
        setLyriaAvailable(lyriaOk);
        setStory(creationStory);
        setMusicPromptText(mp);

        if (audioBase64) {
            setAudioSrc(`data:audio/wav;base64,${audioBase64}`);
        }
        if (coverBase64) {
            setCoverSrc(coverBase64); // already data URI
        }
        if (lyriaError && !lyriaOk) {
            console.warn("Lyria error:", lyriaError);
        }
    }, [musicResult]);

    const togglePlay = () => {
        const audio = audioRef.current;
        if (!audio) return;
        if (isPlaying) {
            audio.pause();
            setIsPlaying(false);
        } else {
            audio.play();
            setIsPlaying(true);
        }
    };

    const handleSave = async () => {
        if (isSaving || saved) return;
        setIsSaving(true);

        const audioBase64 = musicResult?.audioBase64 ?? null;
        const coverBase64 = musicResult?.coverBase64 ?? null;

        if (!audioBase64) {
            alert(language === 'en'
                ? "No audio to save yet. Lyria is not enabled."
                : "Belum ada audio untuk disimpan. Lyria belum diaktifkan.");
            setIsSaving(false);
            return;
        }

        try {
            // 1. Get a signed upload URL and upload audio directly to Supabase
            //    (avoids Vercel's 4.5MB serverless body limit)
            const urlRes = await fetch('/api/music-upload-url');
            const urlData = await urlRes.json();
            if (!urlData.success) throw new Error(urlData.error || 'Failed to get upload URL');

            const audioBytes = Uint8Array.from(atob(audioBase64), c => c.charCodeAt(0));
            const uploadRes = await fetch(urlData.signedUrl, {
                method: 'PUT',
                headers: { 'Content-Type': 'audio/wav' },
                body: audioBytes,
            });
            if (!uploadRes.ok) throw new Error(`Audio upload failed: ${uploadRes.status}`);

            // 2. Save metadata — strip recordedAudioBase64 (large blob, not needed server-side)
            // eslint-disable-next-line @typescript-eslint/no-unused-vars
            const { recordedAudioBase64: _drop, ...stateForSave } = state;
            const response = await fetch('/api/save-music', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({
                    state: stateForSave,
                    audioStoragePath: urlData.path,
                    coverBase64: coverBase64 || null,
                    creationStory: story,
                    musicPrompt: musicPromptText,
                    journey: state.journey,
                }),
            });

            const data = await response.json();
            if (!data.success) {
                alert(`Save failed: ${data.error}`);
                setIsSaving(false);
                return;
            }

            setSaved(true);
            setTimeout(() => {
                resetState();
                router.replace('/gallery');
            }, 1200);
        } catch (err) {
            console.error("Save error:", err);
            alert(language === 'en' ? "An error occurred while saving." : "Terjadi kesalahan saat menyimpan.");
            setIsSaving(false);
        }
    };

    const handleTryAgain = () => {
        resetState();
        router.replace(state.journey === 'sounds' ? '/create/step-1-mood' : '/create/music');
    };

    if (error) {
        return (
            <div className="flex-1 flex flex-col w-full h-full max-w-md mx-auto pt-12 px-5 pb-10 items-center justify-center gap-6 text-center">
                <div className="text-5xl">⚠️</div>
                <h1 className="font-creator text-3xl font-bold text-ink">
                    {language === 'en' ? 'Something went wrong' : 'Terjadi kesalahan'}
                </h1>
                <p className="font-creator text-base text-ink/60 px-4">{error}</p>
                <button onClick={handleTryAgain} className="min-h-[72px] px-10 bg-signal text-ink font-creator font-bold text-xl rounded-creator">
                    {t.tryAgain}
                </button>
            </div>
        );
    }

    return (
        <>
        {showCard && facilitatorPrompt && (
            <FacilitatorPromptCard
                prompt={facilitatorPrompt}
                language={promptLanguage}
                onContinue={dismiss}
                stepLabel={promptLanguage === 'id' ? 'Setelah Membuat' : 'After Creation'}
            />
        )}
        <div className="flex-1 flex flex-col w-full h-full max-w-md mx-auto pt-6 px-5 pb-8 gap-6">

            {/* Title */}
            <div className="text-center">
                <h1 className="font-creator text-3xl font-bold text-ink">{t.title}</h1>
                <p className="font-creator text-sm text-ink/50 mt-1">{t.subtitle}</p>
            </div>

            {/* Cover Art + Player */}
            <div className="relative w-full aspect-square rounded-creator overflow-hidden bg-ink/5 border-2 border-border shadow-md">
                {coverSrc ? (
                    <Image src={coverSrc} alt="Music cover art" fill className="object-cover" priority />
                ) : (
                    <div className="w-full h-full flex items-center justify-center">
                        <svg width="64" height="64" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="1.5" strokeLinecap="round" strokeLinejoin="round" className="text-ink/30">
                            <path d="M9 18V5l12-2v13" />
                            <circle cx="6" cy="18" r="3" />
                            <circle cx="18" cy="16" r="3" />
                        </svg>
                    </div>
                )}

                {/* Play/Pause Overlay */}
                {audioSrc && (
                    <button
                        onClick={togglePlay}
                        className="absolute inset-0 flex items-center justify-center bg-ink/20 backdrop-blur-sm hover:bg-ink/30 transition-all active:scale-95"
                    >
                        <div className="w-20 h-20 rounded-full bg-surface/90 flex items-center justify-center shadow-lg">
                            {isPlaying ? (
                                <svg width="32" height="32" viewBox="0 0 24 24" fill="currentColor" className="text-ink">
                                    <rect x="6" y="4" width="4" height="16" />
                                    <rect x="14" y="4" width="4" height="16" />
                                </svg>
                            ) : (
                                <svg width="32" height="32" viewBox="0 0 24 24" fill="currentColor" className="text-ink ml-1">
                                    <polygon points="5 3 19 12 5 21 5 3" />
                                </svg>
                            )}
                        </div>
                    </button>
                )}

                {audioSrc && (
                    <audio
                        ref={audioRef}
                        src={audioSrc}
                        onEnded={() => setIsPlaying(false)}
                        className="hidden"
                    />
                )}
            </div>

            {/* Lyria unavailable notice */}
            {!lyriaAvailable && (
                <div className="bg-signal/10 border border-signal/30 rounded-creator px-4 py-3">
                    <p className="font-creator text-sm text-ink/80">{t.lyriaUnavailable}</p>
                </div>
            )}

            {/* Source artwork reference */}
            {state.sourceArtworkImageUrl && (
                <div className="flex items-center gap-3">
                    <div className="w-12 h-12 rounded-creator overflow-hidden border border-border flex-shrink-0 relative">
                        <Image src={state.sourceArtworkImageUrl} alt="Source artwork" fill className="object-cover" sizes="48px" />
                    </div>
                    <div>
                        <p className="font-creator text-xs text-ink/50">{t.sourceArtwork}</p>
                        <p className="font-creator text-sm text-ink font-bold truncate">{state.sourceArtworkStory}</p>
                    </div>
                </div>
            )}

            {/* Creation story */}
            {story && (
                <div>
                    <p className="font-creator text-xs text-ink/40 uppercase tracking-wider mb-1">{t.story}</p>
                    <p className="font-creator text-base text-ink italic">&ldquo;{story}&rdquo;</p>
                </div>
            )}

            {/* Sound palette — what the user selected */}
            {state.soundEffects && state.soundEffects.length > 0 && (
                <div>
                    <p className="font-creator text-xs text-ink/40 uppercase tracking-wider mb-2">{t.soundPalette}</p>
                    <div className="flex flex-wrap gap-2">
                        {state.soundEffects.map(s => (
                            <span key={s} className="bg-signal/20 text-ink font-creator text-sm font-bold px-3 py-1 rounded-full capitalize">
                                {s}
                            </span>
                        ))}
                        {state.hasRecordedAudio && (
                            <span className="bg-ink/10 text-ink font-creator text-sm font-bold px-3 py-1 rounded-full">
                                🎙 {language === 'en' ? 'Your Voice' : 'Suaramu'}
                            </span>
                        )}
                    </div>
                </div>
            )}

            {/* Music direction — full text, no truncation */}
            {musicPromptText && (
                <div className="bg-ink/3 rounded-creator px-4 py-3 border border-border">
                    <p className="font-creator text-xs text-ink/40 uppercase tracking-wider mb-1">{t.musicPrompt}</p>
                    <p className="font-creator text-sm text-ink/70">{musicPromptText}</p>
                </div>
            )}

            <SkillsCard
                skills={skills}
                language={language}
                creatorName={sessionData.isActive ? sessionData.creatorName : undefined}
            />

            {/* CTAs */}
            <div className="flex flex-col gap-3 mt-auto">
                <button
                    onClick={handleSave}
                    disabled={isSaving || saved}
                    className="w-full min-h-[72px] bg-signal text-ink font-creator font-bold text-2xl rounded-creator shadow-sm active:scale-[0.98] transition-transform disabled:opacity-70"
                >
                    {saved ? t.saved : isSaving ? t.saving : t.save}
                </button>
                <button
                    onClick={handleTryAgain}
                    className="font-creator text-muted text-lg hover:text-ink active:scale-95 transition-all text-center"
                >
                    {t.tryAgain}
                </button>
            </div>
        </div>
        </>
    );
}
